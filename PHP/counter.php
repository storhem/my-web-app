<?php
// Файл для хранения количества посещений
$file = "counter.txt";

// Если файла нет — создаём с начальным значением 0
if (!file_exists($file)) {
    file_put_contents($file, 0);
}

// Читаем текущее значение
$count = (int) file_get_contents($file);

// Увеличиваем на 1
$count++;

// Записываем новое значение обратно в файл
file_put_contents($file, $count);

// Получаем текущее время (формат ЧЧ:ММ)
$time = date("H:i");

// Выводим сообщение
echo "<h2>Страница была загружена $count раз. Текущее время: $time.</h2>";
?>
